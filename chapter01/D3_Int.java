package c4_package;

public class D3_Int {

	public static void main(String[] args) {

		int var1 = 10; // 십진수
		int var2 = 0b1111; // 2진수
		int var3 = 013; // 8진수			001 011 --> 001011 --> 11
		int var4 = 0xF; // 16진수

		System.out.println("십진수 10은 "+var1);
		System.out.println("2진수 1111은 "+var2);
		System.out.println("8진수 013은 "+var3);
		System.out.println("16진수 F는 "+var4);

	}

}
